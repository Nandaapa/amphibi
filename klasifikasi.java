package amfibi;

public class klasifikasi {

    private String kulit;
    private String darah;
    private String kaki;
    private int jmlKaki;
    private String reproduksi;

    public String getKulit() {
        return kulit;
    }

    public void setKulit(String kulit) {
        this.kulit = kulit;
    }

    public String getDarah() {
        return darah;
    }

    public void setDarah(String darah) {
        this.darah = darah;
    }

    public String getKaki() {
        return kaki;
    }

    public void setKaki(String kaki) {
        this.kaki = kaki;
    }

    public int getJmlKaki() {
        return jmlKaki;
    }

    public void setJmlKaki(int jmlKaki) {
        this.jmlKaki = jmlKaki;
    }

    public String getReproduksi() {
        return reproduksi;
    }

    public void setReproduksi(String reproduksi) {
        this.reproduksi = reproduksi;
    }
    
}  